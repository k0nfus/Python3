# Clear
erste_liste = [1, 2, 3]
print(erste_liste)
erste_liste.clear()
print(erste_liste)

# Pop [removes the index of..]
zweite_liste = [1, 2, 3]
print(zweite_liste)
zweite_liste.pop(1)
print(zweite_liste)
zweite_liste.pop(0)
print(zweite_liste)

# Remove [removes the value of..]
dritte_liste = [1, 2, 3]
dritte_liste.remove(1)
print(dritte_liste)